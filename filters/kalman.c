float invSqrt(float x)
	{
	float halfx = 0.5f * x;
	union 
		{
	  	float    f;
	  	uint32_t i;
		} conv = { .f = x };
	conv.i = 0x5f3759df - (conv.i >> 1);
	conv.f *= 1.5f - (halfx * conv.f * conv.f);
	conv.f *= 1.5f - (halfx * conv.f * conv.f);
	//return conv.f;
	return 1./sqrt(x);
	}

